/** 
    CONSTANTES 
    PARA EL CONTENIDO 
    DEL MENU
**/
const containerMenu = document.querySelector('.Container__Menu')

/** 
    FUNCIONES PARA
    LOS BOTONES RELACIONADOS
    CON EL MENU
**/

// CON ESTA FUNCION MOSTRAREMOS EL MENU
const showMenu = () => {
    containerMenu.classList.add('Show')
}
// CON ESTA FUNCION OCULTAREMOS EL MENU
const closeMenu = () => {
    containerMenu.classList.remove('Show')
}
